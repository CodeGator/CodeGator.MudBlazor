    /// </summary>
    Primary,

    /// <summary>
    /// This enumeration member applies the secondary theme emphasis.
    /// </summary>
    Secondary,

    /// <summary>
    /// This enumeration member applies the tertiary theme emphasis.
    /// </summary>
    Tertiary
}
